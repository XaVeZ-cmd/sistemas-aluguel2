package Ui.Dados.Entidades;

import java.time.LocalDate;

/**
 *
 * @author IFNMG
 */
public class Cliente {
    private Integer idCliente;

    public Integer getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(Integer idCliente) {
        this.idCliente = idCliente;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    public LocalDate getDataNascimemto() {
        return dataNascimemto;
    }

    public void setDataNascimemto(LocalDate dataNascimemto) {
        this.dataNascimemto = dataNascimemto;
    }

    public String getRua() {
        return rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }

    public String getNumeroCasa() {
        return numeroCasa;
    }

    public void setNumeroCasa(String numeroCasa) {
        this.numeroCasa = numeroCasa;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }
    
    private String nome;
    private String cpf;
    private String rg;
    private LocalDate dataNascimemto;
    private String rua;
    private String numeroCasa;
    private String bairro;
    private String cidade;
    
    
    
    
}
